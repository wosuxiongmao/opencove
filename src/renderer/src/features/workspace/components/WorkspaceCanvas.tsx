const TASK_PRIORITY_OPTIONS: Array<{ value: TaskPriority; label: string }> = [
  { value: 'low', label: 'Low' },
  { value: 'medium', label: 'Medium' },
  { value: 'high', label: 'High' },
  { value: 'urgent', label: 'Urgent' },
]

const TASK_PRIORITIES: TaskPriority[] = TASK_PRIORITY_OPTIONS.map(option => option.value)

function normalizeTaskTagSelection(selection: string[], availableTags: string[]): string[] {
  const normalized: string[] = []

  for (const tag of selection) {
    const value = tag.trim()
    if (value.length === 0 || normalized.includes(value)) {
      continue
    }

    if (availableTags.includes(value)) {
      normalized.push(value)
    }
  }

  return normalized
}

function normalizeTaskPriority(value: unknown): TaskPriority {
  if (typeof value !== 'string') {
    return 'medium'
  }

  const normalized = value.trim().toLowerCase()
  return TASK_PRIORITIES.includes(normalized as TaskPriority)
    ? (normalized as TaskPriority)
    : 'medium'
}

function isAgentWorking(status: TerminalNodeData['status']): boolean {
  return status === 'running' || status === 'restoring'
}

function toAgentRuntimeLabel(status: TerminalNodeData['status']): string {
  switch (status) {
    case 'running':
      return 'Working'
    case 'restoring':
      return 'Restoring'
    case 'failed':
      return 'Failed'
    case 'stopped':
      return 'Stopped'
    case 'exited':
      return 'Exited'
    default:
      return 'Idle'
  }
}

function toErrorMessage(error: unknown): string {
  if (error instanceof Error && error.message) {
    return error.message
  }

  if (typeof error === 'string' && error.length > 0) {
    return error
  }

  return 'Unknown error'
}

function providerLabel(provider: AgentProvider): string {
  return AGENT_PROVIDER_LABEL[provider]
}

function providerTitlePrefix(provider: AgentProvider): string {
  return provider === 'codex' ? 'codex' : 'claude'
}

function normalizeDirectoryPath(workspacePath: string, customDirectory: string): string {
  const trimmed = customDirectory.trim()
  if (trimmed.length === 0) {
    return ''
  }

  if (/^([a-zA-Z]:[\\/]|\/)/.test(trimmed)) {
    return trimmed
  }

  const base = workspacePath.replace(/[\\/]+$/, '')
  const normalizedCustom = trimmed.replace(/^[./\\]+/, '')
  return `${base}/${normalizedCustom}`
}

function toSuggestedWorktreePath(workspacePath: string, provider: AgentProvider): string {
  const stamp = new Date().toISOString().replace(/[:.]/g, '-')
  return `${workspacePath}/.cove/worktrees/${providerTitlePrefix(provider)}-${stamp}`
}

function shouldKeepSpace(space: WorkspaceSpaceState): boolean {
  return space.nodeIds.length > 0
}

function sanitizeSpaces(nextSpaces: WorkspaceSpaceState[]): WorkspaceSpaceState[] {
  return nextSpaces
    .map(space => ({
      ...space,
      nodeIds: [...new Set(space.nodeIds)],
    }))
    .filter(shouldKeepSpace)
}

